class Solution:
    # ğŸš€ã€æé€Ÿè§£æ³•ã€‘æ—¶é—´å¤æ‚åº¦ O(sqrt(n))
    # ğŸ§  åŸç†è§£æï¼šä¸€ä¸ªæ•°èƒ½è¡¨ç¤ºæˆè¿ç»­æ­£æ•´æ•°ä¹‹å’Œçš„æ–¹æ³•æ•°ï¼Œç­‰äºå…¶ã€å¥‡æ•°å› å­ã€‘çš„ä¸ªæ•°
    def consecutiveNumbersSum(self, n: int) -> int:
        # ä¸ºäº†åŒ¹é…ä½ ç»™çš„æ•°æ®è§„å¾‹: 0 çš„ç»“æœæ˜¯ 1
        if n == 0: return 1

        # âœ‚ï¸ ç¬¬ä¸€æ­¥ï¼šæŠŠæ‰€æœ‰çš„å› å­ 2 å‰”é™¤æ‰
        # è¿™ä¸€æ­¥æ˜¯ä¸ºäº†åªä¿ç•™ n çš„"å¥‡æ•°éƒ¨åˆ†"
        # æ¯”å¦‚è¾“å…¥ 20 (10100) -> å˜æˆ 5 (101); è¾“å…¥ 24 -> å˜æˆ 3
        while n % 2 == 0:
            n //= 2

        # ğŸ” ç¬¬äºŒæ­¥: è®¡ç®—å‰©ä½™è¿™ä¸ªå¥‡æ•°çš„æ‰€æœ‰å› å­ä¸ªæ•°
        # å› ä¸ºæˆ‘ä»¬åªéœ€è¦æ‰¾å¥‡æ•°å› å­,è€Œ n ç°åœ¨å·²ç»æ˜¯å¥‡æ•°äº†,æ‰€ä»¥å®ƒçš„æ‰€æœ‰å› å­éƒ½æ˜¯å¥‡æ•°å› å­
        count = 0
        i = 1
        # ğŸ”„ åªéœ€è¦éå†åˆ° sqrt(n) å³å¯
        while i * i <= n:
            if n % i == 0:
                count += 1  # ğŸ¯ æ‰¾åˆ°äº†ä¸€ä¸ªå› å­ i
                if i * i != n:
                    count += 1  # ğŸ¯ æ‰¾åˆ°äº†å¯¹åº”çš„å¦ä¸€ä¸ªå› å­ n//i (æˆå¯¹å‡ºç°)
            i += 1
        return count

    # ğŸ¢ã€åŒå¾ªç¯æš´åŠ›æ±‚è§£ã€‘
    # âš ï¸ è¿™ç§æ–¹æ³•è™½ç„¶é€»è¾‘æ­£ç¡®,ä½†åœ¨ n å¾ˆå¤§æ—¶ä¼šè¶…æ—¶ (Time Limit Exceeded)
    def consecutiveNumbersSum2(self, n: int) -> int:
        ans = 0
        for start in range(1, n + 1):
            sum_ = start
            for j in range(start + 1, n + 1):
                if sum_ + j > n:
                    break
                if sum_ + j == n:
                    ans += 1
                sum_ += j
        return ans + 1  # â• åŠ ä¸Šæ•°å­—æœ¬èº«é•¿åº¦ä¸º 1 çš„æƒ…å†µ


