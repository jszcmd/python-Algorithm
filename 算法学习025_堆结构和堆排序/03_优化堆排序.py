# æµ‹è¯•é“¾æ¥:https://leetcode.cn/problems/sort-an-array/
# ç›´æ¥æŠŠä¸‹é¢çš„Solutionç±»å¤åˆ¶åˆ°åŠ›æ‰£é‡Œé¢,pythonæ•ˆç‡ä¸æ˜¯å¤ªé«˜,ä½†æ˜¯æœ‰ä¼˜åŒ–

class Solution:
    def sortArray(self, nums: List[int]) -> List[int]:
        if len(nums) < 1: return nums
        self.heapSort(nums)
        return nums

    # ğŸ”½ å¦‚æœiä½ç½®çš„æ•°å˜å°äº†,é‚£ä¹ˆä¸ºäº†ç»´æŒå¤§æ ¹å †çš„ç‰¹æ€§,arr[i]çš„æ•°å°±è¦å‘ä¸‹æ‰
    # ğŸ“ å½“å‰å †çš„å¤§å°ä¸ºsize
    # â±ï¸ æ—¶é—´å¤æ‚åº¦:O(log(n))
    def heapify(self, arr: list[int], i: int, size: int) -> None:
        l: int = 2 * i + 1  # ğŸ‘ˆ è®¡ç®—å·¦å­©å­ç´¢å¼•ï¼šå®Œå…¨äºŒå‰æ ‘æ€§è´¨
        while l < size:  # ğŸ”„ å½“å­˜åœ¨å·¦å­©å­æ—¶ç»§ç»­å¾ªç¯ï¼ˆæœ‰å­©å­èŠ‚ç‚¹æ‰éœ€è¦å‘ä¸‹è°ƒæ•´ï¼‰
            # ğŸ‘¶ æœ‰å·¦å­©å­: l
            # ğŸ‘¶ å³å­©å­: l+1
            # ğŸ† è¯„é€‰æœ€å¼ºçš„å­©å­æ˜¯å“ªä¸€ä¸ªä¸‹æ ‡çš„å­©å­
            best: int = l + 1 if l + 1 < size and arr[l + 1] > arr[l] else l
            # ğŸ’¡ åˆ¤æ–­é€»è¾‘ï¼š
            #   âœ… å¦‚æœæ—¢æœ‰å³å­©å­,è€Œä¸”å³å­©å­è¿˜æ¯”å·¦å­©å­å¤§,å³å­©å­å°±æ˜¯æœ€å¼ºçš„å­©å­
            #   âœ… å¦‚æœæ²¡æœ‰å³å­©å­,å·¦å­©å­å°±æ˜¯æœ€å¼ºçš„å­©å­
            #   âœ… å¦‚æœæœ‰å³å­©å­,ä½†æ˜¯å³å­©å­æ²¡æœ‰æ¯”å·¦å­©å­å¤§,å·¦å­©å­è¿˜æ˜¯æœ€å¼ºçš„å­©å­

            # ğŸ¥Š ä¸Šè¾¹å·²ç»è¯„é€‰å‡ºäº†æœ€å¼ºçš„å­©å­,å½“å‰çš„æ•°arr[i]å’Œæœ€å¼ºçš„å­©å­ä¹‹é—´,æœ€å¼ºçš„æ˜¯è°
            best = best if arr[best] > arr[i] else i
            if best == i: break  # âœ… æœ€å¼ºçš„æ˜¯ä½ è‡ªå·±,ä½ å°±ä¸ç”¨å¾€ä¸‹æ‰äº†
            # ğŸ”„ ä¸æ¯”æœ€å¼ºçš„å­©å­å¤§,äº¤æ¢ä½ç½®
            arr[i], arr[best] = arr[best], arr[i]
            i = best  # ğŸ“ æ›´æ–°å½“å‰ä½ç½®ä¸ºäº¤æ¢åçš„ä½ç½®
            l = i * 2 + 1  # ğŸ‘‡ é‡æ–°è®¡ç®—å·¦å­©å­ç´¢å¼•ï¼Œç»§ç»­å‘ä¸‹è°ƒæ•´

    def heapSort(self, arr: list[int]) -> None:
        # ğŸ—ï¸ å»ºå †çš„è¿‡ç¨‹O(n) - ä»åº•å‘ä¸Šå»ºå †,æ—¶é—´å¤æ‚åº¦æ›´ä¼˜
        n: int = len(arr)
        # ğŸ”„ ä»æœ€åä¸€ä¸ªéå¶å­èŠ‚ç‚¹å¼€å§‹ï¼Œå‘å‰éå†åˆ°æ ¹èŠ‚ç‚¹
        for i in range(n - 1, -1, -1):
            self.heapify(arr, i, n)  # ğŸ”½ å¯¹æ¯ä¸ªèŠ‚ç‚¹æ‰§è¡Œå‘ä¸‹è°ƒæ•´

        size: int = n  # ğŸ“Š åˆå§‹åŒ–å †å¤§å°
        # ğŸ¯ æ’åºé˜¶æ®µï¼šä¸æ–­å°†æœ€å¤§å€¼ç§»åˆ°æœ«å°¾å¹¶è°ƒæ•´å †
        while size > 1:
            size -= 1  # ğŸ“‰ å †å¤§å°å‡1
            # ğŸ” å°†å †é¡¶æœ€å¤§å€¼ä¸å½“å‰å †æœ«å°¾å…ƒç´ äº¤æ¢
            arr[0], arr[size] = arr[size], arr[0]
            # ğŸ”½ å¯¹æ–°çš„å †é¡¶å…ƒç´ è¿›è¡Œå‘ä¸‹è°ƒæ•´ï¼Œæ¢å¤å¤§æ ¹å †æ€§è´¨
            self.heapify(arr, 0, size)


