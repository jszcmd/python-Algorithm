/*
================================================================================
【核心底层原理与二进制基础】

1. 范围与符号位
   - 没有符号位（无符号）：
     4位  --> 可以表示: 0 到 2^4 - 1
     32位 --> 可以表示: 0 到 2^32 - 1
   - 有符号位：
     4位  --> 负数8个，0和正数8个，范围：-8, -7, ..., -1 和 0, 1, ..., 7
     32位 --> 负数：2^31个 (-2^31 到 -1)，非负数：2^31个 (0 到 2^31 - 1)

2. 二进制与十进制转换规则
   - 已知负数 ---> 求二进制：
     负数 ---> 对应正数的二进制 ----> 减去1 ----> 取反得到的就是(负数在计算机中的存储形式)
     例如：
     -1 ---> 正数1(0001) ----> 减去1(0000) ----> 取反: 1111 (计算机中的存储形式)
     -2 ---> 正数2(0010) ----> 减去1(0001) ----> 取反: 1110
     -7 ---> 正数7(0111) ----> 减去1(0110) ----> 取反: 1001
     -8 ---> 正数8(1000) ----> 减去1(0111) ----> 取反: 1000

   - 已知二进制(负数) ---> 求10进制：
     整个状态连同符号位取反 ---> 加1 ---> 加上负号
     例如：
     1001 (负数) ---> 取反: 0110 ---> 加1: 0111(正数7) ---> 结果为 -7
     1000 (负数) ---> 取反: 0111 ---> 加1: 1000(正数8) ---> 结果为 -8

3. 16进制、10进制与二进制完整对应关系
   - 十六进制(Hex) | 十进制(Dec) | 二进制(Bin)
     -----------------------------------------
     0x0          | 0           | 0000
     0x1          | 1           | 0001
     0x2          | 2           | 0010
     0x3          | 3           | 0011
     0x4          | 4           | 0100
     0x5          | 5           | 0101
     0x6          | 6           | 0110
     0x7          | 7           | 0111
     0x8          | 8           | 1000
     0x9          | 9           | 1001
     0xa          | 10          | 1010
     0xb          | 11          | 1011
     0xc          | 12          | 1100
     0xd          | 13          | 1101
     0xe          | 14          | 1110
     0xf          | 15          | 1111

4. 为什么这样设计二进制（补码）？
   - 目的：让加法和减法在底层使用同样的逻辑电路。以4位有符号数为例：
     正数 + 正数: 1(0001) + 3(0011) = 4(0100)
     正数 + 负数: -5 + 2
          -5 的二进制: 1011 (正数5(0101)->减1(0100)->取反(1011))
           2 的二进制: 0010
          -----------------
          相加得 1101 (负数) ---> 取反(0010) ---> 加1(0011即3) ---> 结果为 -3
     负数 + 负数: -5(1011) + 7(0111) = 10010 ---> 最高位1溢出丢弃 ---> 0010(即2)，结果正确！
================================================================================
*/

#include <stdio.h>
#include <limits.h>
#include <stdbool.h>

// 模拟 Java 的无符号右移 a >>> n
// 强制将 a 转换为无符号类型,这样右移时高位一定会拿 0 来补
#define UR_SHIFT(a, n) ((unsigned int)(a) >> (n))

// =========================================================
// 工具函数：打印数字的 32 位二进制形态
// =========================================================
void printBinary(int num)
{
    for (int i = 31; i >= 0; i--) // 从最高位开始，执行32次比较
    {
        // 判断第 i 位是否为 1
        printf((num & (1 << i)) == 0 ? "0" : "1");

        // 也可以写成: printf((num & (1 << i)) != 0 ? "1" : "0");
        // 注意：写成 printf((num & (1 << i)) == 1 ? "1" : "0") 是错误的，因为结果是 2 的 i 次方，不是 1。
    }
    printf("\n");
}

// 模拟测试用的布尔函数（返回真）
bool returnTrue()
{
    printf("you successfully come in returnTrue;\n");
    return true;
}

// 模拟测试用的布尔函数（返回假）
bool returnFalse()
{
    printf("you successfully come in returnFalse;\n");
    return false;
}

int main()
{
    // =========================================================
    // 模块 1：变量定义与多种进制表示
    // =========================================================
    int a = 78; // 非负数定义
    printf("a : %d\n", a);
    printBinary(a); // 输出 78 的底层二进制：...01001110

    int b = -6; // 负数定义
    printf("b : %d\n", b);
    printBinary(b); // -6 是由 6(0110) 取反加 1 得到，高位全补 1

    int c = 0b1001110; // 0b 开头表示直接写入二进制，等价于十进制的 78
    printf("c : %d\n", c);
    printBinary(c);

    // 0x 开头表示直接写入十六进制
    // 0100 --> 4, 1110 --> e, 所以 0x4e ----> 0b01001110
    int d = 0x4e;
    printf("d : %d\n", d);
    printBinary(d);

    // =========================================================
    // 模块 2：取反与相反数原理
    // =========================================================
    printf("==================\n");
    printf("a original : %d\n", a);
    printBinary(a);  // a 的原码
    printBinary(~a); // ~a 的二进制 (连同符号位全部取反，0 变 1，1 变 0)

    // 相反数核心公式：x 的相反数 = 取反(x) + 1
    // 取反: 0110 0111 ---> 取反: 1001 1000
    int e = ~a + 1; // e 即为 -78
    printf("e (-a) : %d\n", e);
    printBinary(e);

    // =========================================================
    // 模块 3：INT_MIN 的特殊性 (有符号数的最小值)
    // =========================================================
    // 有符号数的最小值是得不到相反数的。
    // 以 4 位为例: 范围 -8 到 7，表示不了 8。-8(1000) -> 取反(0111) -> 加1(1000)，还是 -8 自身。
    // int, long 的最小值，取相反数、绝对值都还是自己本身。
    int f = INT_MIN; // 32位环境下的 -2147483648
    printf("f (INT_MIN) : %d\n", f);
    printBinary(f); // 只有最高位（符号位）是 1，其余全 0

    printf("-f : %d\n", -f);
    printBinary(-f); // 负的 INT_MIN 依然溢出为 INT_MIN

    printf("~f+1 : %d\n", ~f + 1);
    printBinary(~f + 1); // 证明了 ~f + 1 的公式在 INT_MIN 时也符合上述溢出规则

    // =========================================================
    // 模块 4：基础位运算 (|, &, ^)
    // =========================================================

    // 或运算 (|)   : 每一位只要有一个有1，那么这个1就可以留下来
    // 与运算 (&)   : 两个数的这一位都是1，那么这个1才可以留下来
    // 异或运算 (^) : 相同为0，不同为1
    int g = 0b0001010; // 十进制的 10
    int h = 0b0001100; // 十进制的 12
    printf("g | h : \n");
    printBinary(g | h); // 结果应为 0b0001110 (14)
    printf("g & h : \n");
    printBinary(g & h); // 结果应为 0b0001000 (8)
    printf("g ^ h : \n");
    printBinary(g ^ h); // 结果应为 0b0000110 (6)

    // =========================================================
    // 模块 5：逻辑运算符的短路特性 vs 位运算符的无条件执行
    // =========================================================
    // 逻辑运算 (|| 和 &&):
    // - || 逻辑或：只要前面有一个是 true，后面的就不会执行了，结果为 true。
    // - && 逻辑与：只有一路为 true 才会穿透下去；只要前面有一个 false，后面的就不会执行了。
    // 注意：位运算 (| 和 &) 即使被用于逻辑判断，运算符两边的表达式一定都会被执行。

    printf("test1 (Bitwise OR |) begin\n");
    bool test1 = returnTrue() | returnFalse(); // 位运算没有短路，两边都会执行
    printf("test1 : %s\n", test1 ? "true" : "false");

    printf("\ntest2 (Logic OR ||) begin\n");
    bool test2 = returnTrue() || returnFalse(); // 触发短路，因为前面为真，不执行 returnFalse
    printf("test2 : %s\n", test2 ? "true" : "false");

    printf("\ntest3 (Bitwise AND &) begin\n");
    bool test3 = returnFalse() & returnTrue(); // 位运算没有短路，两边都会执行
    printf("test3 : %s\n", test3 ? "true" : "false");

    printf("\ntest4 (Logic AND &&) begin\n");
    bool test4 = returnFalse() && returnTrue(); // 触发短路，因为前面为假，不执行 returnTrue
    printf("test4 : %s\n", test4 ? "true" : "false");

    // =========================================================
    // 模块 6：移位运算 (<<, >>)
    // =========================================================
    printf("\n==================\n");
    // 左移: <<
    int i = 0b0011010; // 原始数据
    printf("original i :\n");
    printBinary(i);
    printf("i << 1     :\n");
    printBinary(i << 1); // 整体向左移动 1 位, 相当于乘 2, 右边补 0
    printf("i << 2     :\n");
    printBinary(i << 2); // 整体向左移动 2 位, 相当于乘 4, 右边补 0
    printf("i << 3     :\n");
    printBinary(i << 3); // 整体向左移动 3 位, 相当于乘 8, 右边补 0

    // 右移: >> (算术右移)，高位使用符号位来补
    // 无符号右移：高位固定使用 0 来补

    // 对于非负数: >> 和 无符号右移 的效果是一样的.
    printf("\nShift positive i :\n");
    printBinary(i);
    printBinary(i >> 2);         // 非负数算术右移，高位补 0
    printBinary(UR_SHIFT(i, 2)); // 逻辑右移，高位也是补 0

    // 对于负数: >> 和 无符号右移 效果完全不同
    int j = 0b11110000000000000000000000000000;
    printf("\nShift negative j :\n");
    printBinary(j);
    printBinary(j >> 2);         // 算术右移：高位拿符号位(1)来补，保持其负数性质
    printBinary(UR_SHIFT(j, 2)); // 逻辑右移：强制拿0来补，负数会瞬间变成极大的正数

    // =========================================================
    // 模块 7：移位与乘除法的数学关系 (仅限非负数)
    // =========================================================
    // 非负数 << 1，等同于乘以 2
    // 非负数 << 2，等同于乘以 4
    // 非负数 << i，等同于乘以 2的i次方
    //
    // 非负数 >> 1，等同于除以 2 (向下取整)
    // 非负数 >> 2，等同于除以 4
    // 非负数 >> i，等同于除以 2的i次方
    // 注意：只有非负数完美符合这个特征，负数的右移取整方向不同，不建议直接等同替代。

    printf("\nMathematical relationship of shifting :\n");
    int k = 10;
    printf("k      : %d\n", k);
    printf("k << 1 : %d\n", k << 1); // 10 * 2 = 20
    printf("k << 2 : %d\n", k << 2); // 10 * 4 = 40
    printf("k << 3 : %d\n", k << 3); // 10 * 8 = 80
    printf("k >> 1 : %d\n", k >> 1); // 10 / 2 = 5
    printf("k >> 2 : %d\n", k >> 2); // 10 / 4 = 2 (整数除法去尾)
    printf("k >> 3 : %d\n", k >> 3); // 10 / 8 = 1 (整数除法去尾)

    return 0;
}

/*
================================================================================
【关于 long 类型的大坑特别提示】

对于 long 类型完全同理，不过要注意：如果是 long 类型的数字 num，有 64 位。
num & (1 << 48) 这种写法是不对的！
因为 1 默认是一个 int 类型，只有 32 位，所以 (1 << 48) 早就溢出了，毫无意义。
必须写成显式的长整型常量：num & (1L << 48)
================================================================================
*/
