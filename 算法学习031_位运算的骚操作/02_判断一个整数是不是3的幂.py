""" 判断一个整数是否为3的幂 """


# https://leetcode.cn/problems/power-of-three/

class Solution:
    def isPowerOfThree(self, n: int) -> bool:
        # 判断一个整数是否为3的幂
        return n > 0 and 1162261467 % n == 0

# 🔍 判断一个数是否为3的幂
#
# 原理：
# 1. 3的幂在32位整数范围内最大的是 3¹⁹ = 1162261467
# 2. 如果一个数是3的幂，那么它一定能被 1162261467 整除
# 3. 反过来，如果 n 能被 1162261467 整除，且 n > 0，那么 n 一定是3的幂
#
# 数学原理：
# - 3的幂在质因数分解中只含有因子3
# - 1162261467 = 3¹⁹，也只含有因子3
# - 如果 n 是3的幂，比如 n = 3ᵏ (k ≤ 19)
#   那么 1162261467 % n = 3¹⁹ % 3ᵏ = 0
# - 如果 n 含有其他质因子（如2、5、7等）
#   那么 1162261467 % n ≠ 0
#
# 例子验证：
# 当 n = 27 (3³)：
#   1162261467 % 27 = 0 ✅
#
# 当 n = 81 (3⁴)：
#   1162261467 % 81 = 0 ✅
#
# 当 n = 18 (2×3²)：
#   1162261467 % 18 ≠ 0 ❌
#
# 当 n = 45 (3²×5)：
#   1162261467 % 45 ≠ 0 ❌
#
# ⏱ 时间复杂度：O(1)（一次取模运算）
# 💾 空间复杂度：O(1)（只使用常量空间）
