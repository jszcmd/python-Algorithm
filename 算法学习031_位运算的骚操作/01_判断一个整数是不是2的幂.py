""" 判断一个整数是不是2的幂 """


# 测试链接:https://leetcode.cn/problems/power-of-two/

class Solution:
    def isPowerOfTwo(self, n: int) -> bool:
        return n > 0 and n == (n & -n)

# 🔍 判断一个数是否为2的幂
#
# 原理：
# 1. 2的幂在二进制表示中只有一个1，其余位都是0
#    例如：1(0001), 2(0010), 4(0100), 8(1000), 16(10000)
#
# 2. n & -n 得到 n 二进制表示中最低位的1
#    -n 是 n 的二进制补码（取反再加1）
#    n & -n 会保留 n 最低位的1，其他位都变为0
#
# 3. 如果 n 是2的幂，那么它只有一个1
#    n & -n 会得到这个1，结果等于 n 本身
#
# 例子验证：
# 当 n = 16 (二进制 10000)：
#   n = 10000
#   -n = 01111 + 1 = 10000 (实际是补码表示，这里是简化)
#   n & -n = 10000 = n ✅
#
# 当 n = 20 (二进制 10100)：
#   n = 10100
#   -n = 01011 + 1 = 01100
#   n & -n = 00100 ≠ n ❌
